""" Example on list data types"""

list = [1,2,3,"A","B"]

print(list)


""" Slicing """

print(list[0:3])


""" again slicing """

print(list[2:])

""" List repetation using * operator"""

print(list*9)


""" again slicing """

print(list[2:4])



""" again slicing """

print(list[:-2])
